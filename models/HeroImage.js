import mongoose from 'mongoose';

const heroImageSchema = new mongoose.Schema({
  image: {
    type: String,
    required: true
  },
  order: {
    type: Number,
    default: 0
  },
  isActive: {
    type: Boolean,
    default: true
  }
}, {
  timestamps: true
});

export default mongoose.model('HeroImage', heroImageSchema);
